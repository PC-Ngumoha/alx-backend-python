#!/usr/bin/env python3
"""
2-floor.py

Contains the floor() which takes a floating point number and
returns the floor (Integer less than or equal to that number)
"""
import math


def floor(n: float) -> int:
    """
    floor(n)

    Args:
      - n -> floating point number

    Return:
      - math.floor(n)
    """
    return math.floor(n)
