#!/usr/bin/env python3
"""
3-to_str.py

Contains the definition of the to_str() function which accepts
a floating point number 'n' and returns its string representation
"""


def to_str(n: float) -> str:
    """
    to_str(n)

    Args:
      - n -> floating point argument

    Return:
      - str(n) -> string representation of n
    """
    return str(n)
