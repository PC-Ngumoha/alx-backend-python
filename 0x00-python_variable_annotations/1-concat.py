#!/usr/bin/env python3
"""
1-concat.py

Contains the function concat() which accepts two strings,
concatenates them and returns the result.
"""


def concat(str1: str, str2: str) -> str:
    """
    concat(str1, str2)

    Args:
      - str1 -> string argument
      - str2 -> string argument

    Return:
      - result of concatenating str1 & str2
    """
    return str1 + str2
