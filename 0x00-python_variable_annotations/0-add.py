#!/usr/bin/env python3
"""
0-add.py

Creates a function that takes in two float args and
returns their sum (another float)
"""


def add(a: float, b: float) -> float:
    """
    add(a, b)

    Args:
      - a -> floating point number
      - b -> floating point number

    Return:
      - sum of a & b -> floating point number
    """
    return a + b
